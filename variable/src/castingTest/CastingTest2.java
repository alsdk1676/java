package castingTest;

public class CastingTest2 {
	public static void main(String[] args) {
//		강제 형변환(문자형) 
		System.out.println('A' + 10); // 65 + 10 = 75
		System.out.println('A' * 1000000 * 'A');
		System.out.println('A' * '?');
		System.out.println('A' == 65); // true
		System.out.println((char)66); // B
	}
}
